public class Declarevariables{
   
    public static void main(String args[]){
       
        int a = 10;
        float b = 3.14f;
        double c = 123.456;
        long d = 1000000L;
        char e  = 'A';
        String f  = "Hello, World!";
        
        System.out.println("Ans a = "+a);
        System.out.println("Ans b = "+b);
        System.out.println("Ans c = "+c);
        System.out.println("Ans d  = "+d);
        System.out.println("Ans e  = "+e);
        System.out.println("Ans f = "+f);
    }
}
