public class LargeNumber{
    
    public static void main(String args[]){
        
        int a = 10;
        int b = 20;
        int c = 30;
        
        int largest = a;
        
        if (b > largest) 
        {
        System.out.println(largest = b);
        }
        if (c > largest) 
        {
        System.out.println(largest = c);
        }
        System.out.println("The largest number is: "+ largest);
        
    }
};